package pojoclasses.pojo;

public class Location {
    private double lat;
    private double lng;

    // Getters e Setters
    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

}